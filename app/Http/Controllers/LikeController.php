    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id_article)
    {
        $like = Like::where(['article_id' => $id_article, 'user_id' => Auth::id()])->get();
        foreach ($like as $item) {
            $item->delete();
        }
        return back();
